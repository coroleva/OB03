# АГРЕГАЦИЯ
# Этот код реализует композицию объектов в ООП с использованием классов, которые описывают различные компоненты компьютера — процессор (CPU) и оперативную память (RAM). Описание каждого класса и процесса:
#
# Класс CPU:
#
# Инициализируется с параметром model, который передаётся при создании объекта и сохраняется как атрибут экземпляра self.model.
# Метод process возвращает строку, указывающую, что процессор работает с конкретной моделью, которая была передана при создании объекта.
# Класс RAM:
#
# Инициализируется с параметром size, который представляет объём оперативной памяти и сохраняется в атрибуте self.size.
# Метод store возвращает строку, которая сообщает, что данные сохраняются в оперативную память объёмом, заданным при инициализации.
# Класс Computer:
#
# Инициализируется с двумя объектами: cpu (процессор) и ram (оперативная память). Эти объекты передаются в конструктор как параметры и сохраняются как атрибуты экземпляра компьютера.
# Метод run вызывает метод process у объекта cpu и метод store у объекта ram, комбинируя результаты в строку, которая сообщает, что компьютер работает, используя указанные компоненты.
# Создание объектов и их использование:
#
# Создаётся объект cpu с моделью "Intel i7".
# Создаётся объект ram с объёмом памяти 16 GB.
# Эти объекты передаются в класс Computer для создания компьютера с указанными характеристиками процессора и памяти.
# Вызов computer.run() выводит строку, которая сообщает, что компьютер использует процессор "Intel i7" и оперативную память на 16 GB.

class CPU:
    def __init__(self, model):
        self.model = model

    def process(self):
        return f"Processing with {self.model} CPU"

class RAM:
    def __init__(self, size):
        self.size = size

    def store(self):
        return f"Storing data in {self.size}GB RAM"

class Computer:
    def __init__(self, cpu, ram):
        self.cpu = cpu
        self.ram = ram

    def run(self):
        return f"{self.cpu.process()} and {self.ram.store()}"

cpu = CPU("Intel i7")
ram = RAM(16)
computer = Computer(cpu, ram)
print(computer.run())
